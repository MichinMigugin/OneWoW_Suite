local function T(key)
    if ns.Constants and ns.Constants.THEME and ns.Constants.THEME[key] then
        return unpack(ns.Constants.THEME[key])
    end
    return 0.5, 0.5, 0.5, 1.0
end

local function S(key)
    if ns.Constants and ns.Constants.SPACING then
        return ns.Constants.SPACING[key] or 8
    end
    return 8
end

ns.T = T
ns.S = S

function ns.ApplyTheme()
    local OneWoW_GUI = LibStub("OneWoW_GUI-1.0", true)
    local themeKey
    if OneWoW_GUI and OneWoW_GUI.GetSetting then
        themeKey = OneWoW_GUI:GetSetting("theme")
    end
    themeKey = themeKey or "green"

    if ns.Constants.THEMES and ns.Constants.THEMES[themeKey] then
        local selectedTheme = ns.Constants.THEMES[themeKey]
        for key, value in pairs(selectedTheme) do
            if key ~= "name" then
                ns.Constants.THEME[key] = value
            end
        end
    end

    if OneWoW_GUI then
        OneWoW_GUI:ApplyTheme()
    end
end

function ns.GetVersionString()
    return ns.Constants.VERSION
