D.DYNAMIC_API_NAMES = {}
D.apiDocLoaded = false

function D:LoadAPIDocumentation()
    if self.apiDocLoaded then return end
    self.apiDocLoaded = true

    local ok = pcall(C_AddOns.LoadAddOn, "Blizzard_APIDocumentation")
    if not ok or not APIDocumentation or not APIDocumentation.systems then return end

    for _, system in ipairs(APIDocumentation.systems) do
        if system.Functions then
            for _, func in ipairs(system.Functions) do
                if func.Name then
                    if system.Namespace then
                        self.DYNAMIC_API_NAMES[system.Namespace .. "." .. func.Name] = true
                    else
                        self.DYNAMIC_API_NAMES[func.Name] = true
                    end
                end
            end
        end
    end
end

